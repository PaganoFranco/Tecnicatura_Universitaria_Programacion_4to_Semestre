# Profundizando en los sistemas de numeración

# Sistema decimal
a = 10
print(f"a decimal: {a}")

# Sistema binario
a = 0b1010 # El "0b" hace referencia a número binario
print(f"a binario: {a}")

# Sistema octal
a = 0o12 # El "0o" hace referencia al sistema octal
print(f"a octal: {a}")

# Sistema hexadecimal
a = 0xA # El "0x" hace referencia al sistema hexadecimal
print(f"a hexadecimal: {a}")
