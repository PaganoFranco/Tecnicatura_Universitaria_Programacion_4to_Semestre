# Profundizando en los sistemas de numeración

# Sistema decimal
a = 10
print(f"a decimal: {a}")

# Sistema binario
a = 0b1010 # El "0b" hace referencia a número binario
print(f"a binario: {a}")

# Sistema octal
a = 0o12 # El "0o" hace referencia al sistema octal
print(f"a octal: {a}")

# Sistema hexadecimal
a = 0xA # El "0x" hace referencia al sistema hexadecimal
print(f"a hexadecimal: {a}")

# Base Decimal
a = int("10", 10)
print(f"a = base decimal: {a}")

# Base Binario
a = int("1010", 2)
print(f"a = base binaria: {a}")

# Base Octal
a = int("12", 8)
print(f"a = base octal: {a}")

# Base Hexadecimal
a = int("A", 16)
print(f"a = base hexagecimal: {a}")

# Base 5 sus valores son de 0 a 4
a = int("20", 5)
print(f"a = base 5: {a}")